import java.util.*;
import java.lang.*;
import java.io.*;

class Codechef
{
	public static void main (String[] args) throws java.lang.Exception
	{
		Scanner sc=new Scanner(System.in);
		int t=sc.nextInt();
		for(int i=0;i<t;i++)
		{
		    long n = sc.nextLong();
	        if(n == 0)
	        {
	            System.out.println(-1);
	        }
	        else
	        {
	            System.out.println(0+" "+n);
	        }
		    
		}

	}
}
